const cart = [];

// Function to add book to the cart
function addToCart(bookId, title, price) {
  const book = { id: bookId, title, price };
  cart.push(book);
  displayCart();
}

// Function to display cart items
function displayCart() {
  const cartList = document.getElementById('cart-list');
  cartList.innerHTML = ''; // Clear existing list

  cart.forEach(book => {
    const listItem = document.createElement('li');
    listItem.textContent = `${book.title} - $${book.price}`;
    cartList.appendChild(listItem);
  });
}

// Add event listeners to all "Add to Cart" buttons
document.querySelectorAll('.btn').forEach(button => {
  button.addEventListener('click', function() {
    const bookItem = this.parentElement;
    const bookId = bookItem.getAttribute('data-id');
    const title = bookItem.getAttribute('data-title');
    const price = bookItem.getAttribute('data-price');

    addToCart(bookId, title, price);
  });
});
